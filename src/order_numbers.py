
def order_numbers(numbers):
    """
    Takes a list of numbers and returns a list of the numbers in ascending order.
    """

    return sorted(numbers)



def get_first_number(numbers):
    """
    Takes a list of numbers and returns the first number in the list.
    """

    return numbers[0]

def sum_numbers(numbers):
    """
    Takes a list of numbers and returns the sum of all the numbers.
    """

    return sum(numbers)

